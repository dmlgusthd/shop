package goods.service;

public class Goods {
	private int gCode=0;
	private String gName;
	private int gPrice =0;
	private int gStock=0;
	private String gColor;
	private String gImgsrc;
	private String gThumb;
	private String gCategory;
	
	public int getgCode() {
		return gCode;
	}
	public void setgCode(int gCode) {
		this.gCode = gCode;
	}
	public String getgName() {
		return gName;
	}
	public void setgName(String gName) {
		this.gName = gName;
	}
	public int getgPrice() {
		return gPrice;
	}
	public void setgPrice(int gPrice) {
		this.gPrice = gPrice;
	}
	public int getgStock() {
		return gStock;
	}
	public void setgStock(int gStock) {
		this.gStock = gStock;
	}
	public String getgColor() {
		return gColor;
	}
	public void setgColor(String gColor) {
		this.gColor = gColor;
	}
	public String getgImgsrc() {
		return gImgsrc;
	}
	public void setgImgsrc(String gImgsrc) {
		this.gImgsrc = gImgsrc;
	}
	public String getgThumb() {
		return gThumb;
	}
	public void setgThumb(String gThumb) {
		this.gThumb = gThumb;
	}
	public String getgCategory() {
		return gCategory;
	}
	public void setgCategory(String gCategory) {
		this.gCategory = gCategory;
	}
	
	
}
